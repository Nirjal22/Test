public class Operators {
    public static void main(String[] args) {
        int a = 30/5-4*2;
        System.out.println(a);
        int b = 2*15+10/5;
        System.out.println(b);
        //Increment and Decrement operators
        int i = 55;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(++i);
    }
}
